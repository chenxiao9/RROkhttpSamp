public class Point {
    private float x;
    private float y;

    public Point() {
    }

    public Point(float y, float x) {
        this.y = y;
        this.x = x;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
